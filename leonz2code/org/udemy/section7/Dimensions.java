package leonz2code.org.udemy.section7;

public class Dimensions {

	private int width;
	private int height;
	private int depth;
	public Dimensions(int width, int height, int depth) {
		super();
		this.width = width;
		this.height = height;
		this.depth = depth;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	public int getDepth() {
		return depth;
	}
	
	
}
